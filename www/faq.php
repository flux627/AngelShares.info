<p><b>Q: What happens when I send a donation?</b></p>
<p><b>A:</b> Donations are logged in the public block chain to be referenced every time a new DAC is released. This website keeps track of the donations to both Bitcoin and ProtoShare blockchains, and provides the information in an easy-to-access format.</p>
</br>
<p><b>Q: Are AngelShares transferrable?</b></p>
<p><b>A:</b>No. Since each balance is tied to a wallet address, there is no way to transfer a wallet without having trust that the other party hasn't copied the private keys.</p>
</br>
<p><b>Q: Can I donate from a multi-address PTS/BTC wallet?</b></p>
<p><b>A:</b> Yes. When the time comes to claim your distribution share, there will be tools to import your wallet.</p>
</br>
<p><b>Q: Can I send donations from an exchange?</b></p>
<p><b>A:</b> No, you have to have control over your private keys for the addresses you donate from. It is best to send your ProtoShares and Bitcoin from an encrypted wallet located on your computer.</p>
</br>
<p><b>Q: Will AGS be sent to me?</b></p>
<p><b>A:</b> No. The proof that exists of the transactions to the Angel Addresses in the ProtoShare and Bitcoin blockchains are used to determine DAC distribution shares. You own your AngelShares as much as you own the private keys in your wallets.</p>
</br>
<p><b>Q: What will the funds for AngelShares be used for?</b></p>
<p><b>A:</b> 100% of the funds will be used to fund production of new DACs. 0% is kept as profits from Invictus Innovations.</p>
</br>

