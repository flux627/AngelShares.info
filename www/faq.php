<h1>FAQ</h1>

<h2>General Questions</h2>



<p><b>Q: What are AngelShares?</b></p>
<p><b>A:</b> AngelShares is an initiative by Invictus Innovations that are a way to direct resources currently wasted on mining equipment and electricity and direct them into helping the crypto-equity industry grow.</p>
</br>
<p><b>Q: How do does the daily AngelShares auction work?</b></p>
<p><b>A:</b> Every day until July 20 2014, 10,000 AGS will be given out to donators of Bitcoin and ProtoShares- 5000 per currency. For each day, the total amount of BTC and PTS are divided by 5000, and the amount that you donated in this day period is multiplied by this number to yeild your AGS share. </p>
</br>
<p><b>Q: How are the days split up, exactly?</b></p>
<p><b>A:</b> Every day is split between midnight-to-midnight UTC time. More precisely, the donation transactions count toward the day of the UCT timestamp of the block that the transaction is included in.</p>
</br>
<p><b>Q: How many AngelShares will there be?</b></p>
<p><b>A:</b> 10,000 AGS * 200 days = 2,000,000</p>
</br>
